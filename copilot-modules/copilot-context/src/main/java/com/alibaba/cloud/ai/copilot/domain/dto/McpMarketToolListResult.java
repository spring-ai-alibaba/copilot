package com.alibaba.cloud.ai.copilot.domain.dto;

import com.alibaba.cloud.ai.copilot.domain.entity.McpMarketTool;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.util.List;

/**
 * MCP 市场工具列表返回结果（分页）
 *
 * @author copilot team: evo
 * @email exotisch@163.com
 */
@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class McpMarketToolListResult {

    /**
     * 是否成功
     */
    private boolean success;

    /**
     * 工具列表
     */
    private List<McpMarketTool> data;

    /**
     * 总数
     */
    private long total;

    /**
     * 当前页
     */
    private int page;

    /**
     * 每页大小
     */
    private int size;

    /**
     * 总页数
     */
    private long pages;

    public static McpMarketToolListResult of(List<McpMarketTool> data, long total, int page, int size) {
        long pages = (total + size - 1) / size;
        return McpMarketToolListResult.builder()
                .success(true)
                .data(data)
                .total(total)
                .page(page)
                .size(size)
                .pages(pages)
                .build();
    }
}

